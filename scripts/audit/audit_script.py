def generate_csv(filename):
    header = "letter,ThetaHat,ThetaDeg,ThetaRad,nt,nf,nm,km,kt,kd,ka,kz,qa,qt,qd,qs,qz,U,rho,AN,AK,AQ,hamzah_marker"
    rows = []
    for item in data_raw:
        letter = item[0]
        row = [
            letter, item[1], item[2], item[3],
            item[4][0], item[4][1], item[4][2],
            item[5][0], item[5][1], item[5][2], item[5][3], item[5][4],
            item[6][0], item[6][1], item[6][2], item[6][3], item[6][4],
            item[7], item[8], item[9], item[10], item[11], item[12]
        ]
        rows.append(",".join(map(str, row)))
    
    with open(filename, "w", encoding="utf-8", newline="\n") as f:
        f.write(header + "\n")
        f.write("\n".join(rows) + "\n")

def generate_normaliz(filename):
    with open(filename, "w", encoding="utf-8", newline="\n") as f:
        f.write("amb_space 18\n")
        f.write("cone 28\n")
        for item in data_raw:
            letter = item[0]
            # Order: th_hat, nt, nf, nm, km, kt, kd, ka, kz, qa, qt, qd, qs, qz, an, ak, aq, marker
            marker = 1 if letter == "ك" else 0
            coords = [
                item[1],
                item[4][0], item[4][1], item[4][2],
                item[5][0], item[5][1], item[5][2], item[5][3], item[5][4],
                item[6][0], item[6][1], item[6][2], item[6][3], item[6][4],
                item[9], item[10], item[11], marker
            ]
            f.write(" ".join(map(str, coords)) + "\n")

def get_sha256(filename):
    sha256_hash = hashlib.sha256()
    with open(filename, "rb") as f:
        for byte_block in iter(lambda: f.read(4096), b""):
            sha256_hash.update(byte_block)
    return sha256_hash.hexdigest()

